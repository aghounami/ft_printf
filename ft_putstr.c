#include "ft_printf.h"

void	ft_putstr(char *st, int *l)
{
	int	i;

	i = 0;
	if (!st)
		st = "(null)";
	while (st[i] != '\0')
	{
		ft_putchar(st[i], l);
		i++;
	}
}
